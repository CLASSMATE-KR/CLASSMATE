// 문제 데이터 (파싱된 문제들)

export interface Problem {
  id: number
  title: string
  content: string
  options: string[]
  correctAnswer: number // 0-3 (①-④)
  subject: string
  difficulty: 'easy' | 'medium' | 'hard'
  category: string
}

// 문제 파싱 함수
export function parseProblemText(text: string): Problem[] {
  const problems: Problem[] = []
  const lines = text.split('\n').filter(line => line.trim())
  
  let currentCategory = ''
  
  for (const line of lines) {
    // 카테고리 감지
    if (line.includes('##')) {
      currentCategory = line.replace(/##\s*/, '').trim()
      continue
    }
    
    // 문제 번호 감지 (숫자. 로 시작)
    // "=" 또는 "?"로 끝나는 문제 형식 지원
    const problemMatch = line.match(/^(\d+)\.\s*(.+?)\s*(?:=\s*)?\?\s*(.+)$/)
    if (problemMatch) {
      const problemNumber = parseInt(problemMatch[1])
      const questionText = problemMatch[2].trim()
      const optionsText = problemMatch[3].trim()
      
      // 선택지 파싱 (①, ②, ③, ④)
      const options: string[] = []
      const optionRegex = /[①②③④]\s*([^①②③④]+)/g
      let match
      while ((match = optionRegex.exec(optionsText)) !== null) {
        options.push(match[1].trim())
      }
      
      // 난이도 결정
      let difficulty: 'easy' | 'medium' | 'hard' = 'medium'
      if (currentCategory.includes('기초') || problemNumber <= 50) {
        difficulty = 'easy'
      } else if (problemNumber <= 150) {
        difficulty = 'medium'
      } else {
        difficulty = 'hard'
      }
      
      // 정답은 나중에 수동으로 설정 (임시로 0번)
      const correctAnswer = 0
      
      // 문제 내용 형식 결정 (수식이면 "=" 포함, 아니면 "?"만)
      const content = questionText.includes('=') 
        ? `${questionText} = ?` 
        : `${questionText}?`
      
      problems.push({
        id: problemNumber,
        title: `문제 ${problemNumber}`,
        content: content,
        options: options,
        correctAnswer: correctAnswer,
        subject: '수학',
        difficulty: difficulty,
        category: currentCategory
      })
    }
  }
  
  return problems
}

